a = "Hello"
print(type(a))

b = 5
print(type(b))

c = 5.5
print(type(c))

d = 5j
print(type(d))

e = ["Apple","Banana"]
print(type(e))

f = ("Apple","Banana")
print(type(f))

g = {"Apple","Banana"}
print(type(g))

h = {"first":"Apple","second":"Banana"}
print(type(h))

i = frozenset({"Apple","Banana"})
print(type(i))

j = None
print(type(j))

k = b"Hello"
print(type(k))

l = bytearray(5)
print(type(l))

m = memoryview(bytes(5))
print(type(m))

n = range(6)
print(type(n))


x = str(5)
print(type(x))