# Integers
a = int(1) # int 1
b = int(2.8) # int 2
c = int("3")   # int 3


# Floats
d = float(1)   # float 1.0
e = float(2.8)  # float 2.8
f = float("3")   # float 3.0
g = float("3.5")   # float 3.5


# String
h = str("S1")   # str S1
i = str(2)  # str "2"
j = str(4.2)   # str '4.2'


print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)
print(j)
